    class Meta:
        verbose_name = "Manutenção de Viatura"
        verbose_name_plural = "Manutenções de Viatura"
        ordering = ['-data_agendada']

    def __str__(self):
        return f"Manutenção {self.viatura} - {self.data_agendada}"

    @property
    def custo_previsto(self):
        """Retorna o custo real, ou estimado, ou o preço definido no contrato"""
        if self.custo_real:
            return self.custo_real
        if self.custo_estimado:
            return self.custo_estimado
            
        # Tenta buscar do contrato
        if self.contrato and self.tipo_manutencao:
            try:
                preco_obj = self.contrato.precos_servicos.filter(tipo_servico=self.tipo_manutencao).first()
                if preco_obj:
                    return preco_obj.preco
            except:
                pass
        return None

    @property
    def oficina_nome(self):
        """Retorna o nome da oficina contratada ou o texto manual"""
        if self.fornecedor:
            return self.fornecedor.nome
        return self.oficina or "Não definida"

