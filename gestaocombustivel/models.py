    descricao = models.CharField(max_length=200, verbose_name="Descrição/Objeto do Contrato")
    
    valor_total = models.DecimalField(max_digits=15, decimal_places=2, verbose_name="Valor Total do Contrato")
    valor_gasto = models.DecimalField(max_digits=15, decimal_places=2, default=0, verbose_name="Valor Executado")
    
    data_inicio = models.DateField(verbose_name="Data Início")
    data_fim = models.DateField(verbose_name="Data Fim")
    activo = models.BooleanField(default=True, null=True, verbose_name="Ativo")
    
    documento = models.FileField(upload_to='contratos/manutencao/', blank=True, verbose_name="Documento Digital")
    
