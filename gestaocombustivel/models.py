    def calcular_consumo_medio(self, periodo_dias=30):

















        from django.db.models import Sum



































        data_inicio = date.today() - timedelta(days=periodo_dias)



































        pedidos = self.pedidos_combustivel.filter(

















            status='abastecido',

















            data_abastecimento__gte=data_inicio

















        ).order_by('data_abastecimento')



































        if pedidos.count() < 2:

















            return 0



































        primeiro = pedidos.first()

















        ultimo = pedidos.last()



































        if not primeiro or not ultimo:

















            return 0



































        if primeiro.kilometragem_actual is None or ultimo.kilometragem_actual is None:

















            return 0



































        km_percorridos = float(ultimo.kilometragem_actual) - float(primeiro.kilometragem_actual)

















        total_litros = float(pedidos.aggregate(Sum('quantidade_litros'))['quantidade_litros__sum'] or 0)



































        if km_percorridos > 0 and total_litros > 0:

















            return round(km_percorridos / total_litros, 2)



































        return 0



































